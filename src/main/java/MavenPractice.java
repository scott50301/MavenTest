public class MavenPractice {
    public double add (double n1, double n2){
        return n1 + n2;
    }
    public double minus (double n1, double n2){
        return n1 - n2;
    }
    public double produce (double n1, double n2){
        return n1 * n2;
    }
    public double divide (double n1, double n2){
        return n1 / n2;
    }
}
